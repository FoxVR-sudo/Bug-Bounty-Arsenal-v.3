def _env_bool(name: str, default: bool = False) -> bool:
    """Parse boolean environment variables.

    Accepts: 1/0, true/false, yes/no, on/off (case-insensitive).
    """

    value = os.getenv(name)
    if value is None:
        return bool(default)
    return str(value).strip().lower() in ('1', 'true', 'yes', 'on')


def _is_running_tests() -> bool:
    """Best-effort detection for test runs.

    CI often runs with `DEBUG=False` even for unit tests. We still want strict
    production validations, but they should not block `manage.py test` or
    pytest runs.
    """

    if os.getenv('PYTEST_CURRENT_TEST'):
        return True
    if 'pytest' in sys.modules:
        return True
    argv = [a.lower() for a in sys.argv if isinstance(a, str)]
    return 'test' in argv or 'pytest' in argv


