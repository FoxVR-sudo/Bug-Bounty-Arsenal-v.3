# 0) Resend HTTP API (anymail) — no SMTP ports, works on any VPS
# 1) Generic SMTP (SMTP_HOST + SMTP_USER + SMTP_PASSWORD) — Gmail, Brevo, Outlook, etc.
# 2) Mailtrap SMTP (MAILTRAP_HOST + MAILTRAP_USER + MAILTRAP_PASSWORD)
# 3) SendGrid API key
# 4) Console (dev-friendly fallback — prints emails to terminal, nothing is sent)
